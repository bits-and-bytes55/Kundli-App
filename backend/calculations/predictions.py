def generate_predictions(planets, ascendant, house_positions):
    predictions = []
    
    # 1. Lagna Prediction
    lagna_rashi = ascendant.get('rashi', 'Mesh')
    lag_pred = LAGNA_PREDICTIONS.get(lagna_rashi, LAGNA_PREDICTIONS['Mesh'])
    predictions.append({
        'title_hi': f'लग्न फल - {lagna_rashi}',
        'title_en': f'Lagna prediction - {lagna_rashi}',
        'desc_hi': lag_pred['hi'],
        'desc_en': lag_pred['en']
    })
    
    # 2. Moon Sign (Rashi) Prediction
    moon_rashi = planets.get('Moon', {}).get('rashi', 'Mesh')
    rashi_pred = RASHI_PREDICTIONS.get(moon_rashi, RASHI_PREDICTIONS['Mesh'])
    predictions.append({
        'title_hi': f'चन्द्र राशि फल - {moon_rashi}',
        'title_en': f'Moon Sign prediction - {moon_rashi}',
        'desc_hi': rashi_pred['hi'],
        'desc_en': rashi_pred['en']
    })
    
    # 3. Nakshatra Prediction
    moon_nak = planets.get('Moon', {}).get('nakshatra', 'Ashwini')
    nak_pred_str_en = f"You are born under {moon_nak} Nakshatra. This gives you a unique mental template, sharp intuition, and distinct life path."
    nak_pred_str_hi = f"आपका जन्म {moon_nak} नक्षत्र में हुआ है। यह आपको एक अद्वितीय मानसिक ढांचा, तीव्र अंतर्ज्ञान और स्पष्ट जीवन पथ प्रदान करता है।"
    predictions.append({
        'title_hi': f'नक्षत्र फल - {moon_nak}',
        'title_en': f'Nakshatra prediction - {moon_nak}',
        'desc_hi': nak_pred_str_hi,
        'desc_en': nak_pred_str_en
    })
    
    # 4. Planets in Houses
    planets_list = ['Sun', 'Moon', 'Mars', 'Mercury', 'Jupiter', 'Venus', 'Saturn', 'Rahu', 'Ketu']
    for p in planets_list:
        house = house_positions.get(p)
        if house is not None:
            p_preds = PLANET_HOUSE_PREDICTIONS.get(p, {}).get(house)
            if p_preds:
                predictions.append({
                    'title_hi': f'{p} - {house} भाव फल',
                    'title_en': f'{p} in {house} House',
                    'desc_hi': p_preds['hi'],
                    'desc_en': p_preds['en']
                })
                
    return predictions
